(defn tile-set
  "set of unique MODIS tiles for the specified
  countries (union). Example usage:

    (tile-set [8 6] :IDN :MYS :PHL)"
  [& inputs]
  (->> inputs
       (map #(if (keyword? %)
               (country-tiles %)
               #{%}))
       (apply union)))


